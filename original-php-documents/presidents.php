<h3>Elizabeth Warren</h3>

<h4>Summary</h4>

<p>
Unable to surmount Republican opposition to become the Director of the CFPB, Warren instead challenged and defeated Republican incumbent Senator Scott Brown of Massachusetts in 2012. She was the keynote speaker of the 2016 Democratic National Convention and became Vice Chair of the Senate Democratic Caucus the following year. A noted progressive leader, Warren has focused on consumer protection, economic opportunity, and the social safety net while in the Senate. </p>

<h4>Early Life</h4>

<p>Warren was born Elizabeth Ann Herring in Oklahoma City on June 22, 1949, the fourth child of middle-class parents Pauline (née Reed, 1912–1995) and Donald Jones Herring (1911–1997). Warren has described her family as teetering "on the ragged edge of the middle class" and "kind of hanging on at the edges by our fingernails".</p>
