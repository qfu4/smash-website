<h4>Summary</h4>

<p>Kirsten Elizabeth Gillibrand is an American attorney and politician serving as the junior United States Senator from New York since January 2009. She previously held the position of U.S. Representative for New York's 20th congressional district from 2007 until her Senate appointment. Gillibrand is a member of the Democratic Party.

On January 15, 2019, Gillibrand announced an exploratory committee for her potential candidacy for President of the United States in 2020.</p>

